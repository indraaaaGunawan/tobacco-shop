    private void btn_edtProfilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_edtProfilActionPerformed
        String strid_user = txtIDuser.getText();
        int IdUser = Integer.parseInt(strid_user);
        String nama_user = txtNamaUser.getText();
        String email = txtEmail.getText();
        String alamat = txtAlamat.getText();
        String notlp = txtNotlp.getText();
        String User = txtUser.getText();
        String Pass = txtpassP.getText();
       
        Customer Cstm = new Customer(IdUser, nama_user, alamat, notlp, email, User, Pass);
        Execute.ExecutCustomer eCstm = new Execute.ExecutCustomer();
        int hasil = eCstm.updateCstmr(IdUser, nama_user, alamat, notlp, email, User, Pass);
        if (hasil > 0){
        JOptionPane.showMessageDialog(null, "Perubahan Berhasil, Silahkan Keluar dan Masuk Kembali");
       }
        else{
          JOptionPane.showMessageDialog(null, "Perubahan Gagal");  
        }
    }//GEN-LAST:event_btn_edtProfilActionPerformed

