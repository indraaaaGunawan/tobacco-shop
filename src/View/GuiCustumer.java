    private void btn_ConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ConfirmActionPerformed
        int totalbrg = Integer.parseInt(txtTtlBrg.getText()) ;
        int totalhrg = Integer.parseInt(txtTtlHrg.getText());
        String tanggal = dateChooserTgl.getText();
        String idbrg = txtIDbrg.getText();
        int idCtm = Integer.parseInt(txtIdCus.getText());
        
        Transaksi tran = new Transaksi(totalbrg, totalhrg,tanggal,idbrg,idCtm);
        Execute.ExecutTransaksi eTran = new Execute.ExecutTransaksi();
        int hasil = eTran.insertTrans(tran);
        if (hasil > 0){
        JOptionPane.showMessageDialog(null, "Transaksi Sukses");
        }
        else{
            
          JOptionPane.showMessageDialog(null, "Transaksi Failed");  
        }
    }//GEN-LAST:event_btn_ConfirmActionPerformed

